# ++ (Pre/Post Increment), -- (Pre/Post Decrement)

$a = 20;


$a++; // Post Increment

echo $a;

echo "<br>";

$b = 20;

++$b; // Pre Increment

echo $b;


echo "<br><br>";

$post = 20;

echo ++$post;

// ======================
echo "<br><br>";

$pre = 30;

$pre--;

echo $pre;









